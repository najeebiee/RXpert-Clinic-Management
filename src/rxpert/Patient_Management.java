    // Add a new patient to the JSON file
    private void addPatient() {
        File file = new File(filePath);
        JSONObject jsonObject = new JSONObject();
        JSONArray patientList = new JSONArray();

        // Read existing JSON file
        if (file.exists() && file.length() > 0) {
            try (FileReader reader = new FileReader(file)) {
                JSONParser parser = new JSONParser();
                jsonObject = (JSONObject) parser.parse(reader);
                patientList = (JSONArray) jsonObject.get("patientList");
            } catch (IOException | ParseException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error reading patient records.");
                return;
            }
        }

        // Create a new patient object with data from input fields
        JSONObject patient = new JSONObject();
        patient.put("id", String.valueOf(patientList.size() + 1));
        patient.put("name", jTextField1.getText()); // Patient Name
        patient.put("age", jTextField4.getText()); // Age
        patient.put("email", jTextField2.getText()); // Email
        patient.put("phone", jTextField7.getText()); // Phone
        patient.put("gender", jComboBox1.getSelectedItem().toString()); // Gender
        patient.put("address", jTextField6.getText()); // Address
        patient.put("medicalHistory", jTextField8.getText()); // Medical History
        patient.put("dateOfBirth", jTextField3.getText()); // Date of Birth

        patientList.add(patient);
        jsonObject.put("patientList", patientList);

        // Write updated JSON back to file
        try (FileWriter writer = new FileWriter(file)) {
            writer.write(jsonObject.toJSONString());
            writer.flush();
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving patient record.");
            return;
        }

        clearAddPatientFields();
        JOptionPane.showMessageDialog(this, "Patient added successfully!");
        loadPatients();
    }

