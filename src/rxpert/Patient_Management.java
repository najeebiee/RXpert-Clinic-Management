    private void applyGradient(javax.swing.JPanel panel, java.awt.Color startColor, java.awt.Color endColor, boolean isHorizontal) {
        panel.setOpaque(false);
        panel.setUI(new javax.swing.plaf.PanelUI() {
            @Override
            public void update(java.awt.Graphics g, javax.swing.JComponent c) {
                java.awt.Graphics2D g2d = (java.awt.Graphics2D) g;
                int width = c.getWidth();
                int height = c.getHeight();
                java.awt.GradientPaint gp = isHorizontal
                    ? new java.awt.GradientPaint(0, 0, startColor, width, 0, endColor)
                    : new java.awt.GradientPaint(0, 0, startColor, 0, height, endColor);
                g2d.setPaint(gp);
                g2d.fillRect(0, 0, width, height);
                super.update(g, c);
            }
        });
    }



