    // Load patient details into the "Edit Patient" tab
    private void loadPatientDetails(String enteredName) {
        try (FileReader reader = new FileReader(filePath)) {
            JSONParser parser = new JSONParser();
            JSONObject jsonObject = (JSONObject) parser.parse(reader);
            JSONArray patientList = (JSONArray) jsonObject.get("patientList");

            for (Object obj : patientList) {
                JSONObject patient = (JSONObject) obj;
                if (patient.get("name").toString().equalsIgnoreCase(enteredName)) {
                    jTextField5.setText(patient.get("name").toString());
                    jTextField11.setText(patient.get("age").toString());
                    jTextField9.setText(patient.get("email").toString());
                    jTextField13.setText(patient.get("phone").toString());
                    jComboBox2.setSelectedItem(patient.get("gender").toString());
                    jTextField12.setText(patient.get("address").toString());
                    jTextField14.setText(patient.get("medicalHistory").toString());
                    jTextField10.setText(patient.get("dateOfBirth").toString());
                    return;
                }
            }

            JOptionPane.showMessageDialog(this, "Patient not found!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    // Update patient details in the JSON file
    private void updatePatient() {
        String enteredName = jTextField5.getText().trim();

        if (enteredName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter a patient's name to update.");
            return;
        }

        try (FileReader reader = new FileReader(filePath)) {
            JSONParser parser = new JSONParser();
            JSONObject jsonObject = (JSONObject) parser.parse(reader);
            JSONArray patientList = (JSONArray) jsonObject.get("patientList");

            boolean found = false;

            for (int i = 0; i < patientList.size(); i++) {
                JSONObject patient = (JSONObject) patientList.get(i);

                if (patient.get("name").toString().equalsIgnoreCase(enteredName)) {
                    patient.put("name", jTextField5.getText());
                    patient.put("age", jTextField11.getText());
                    patient.put("email", jTextField9.getText());
                    patient.put("phone", jTextField13.getText());
                    patient.put("gender", jComboBox2.getSelectedItem().toString());
                    patient.put("address", jTextField12.getText());
                    patient.put("medicalHistory", jTextField14.getText());
                    patient.put("dateOfBirth", jTextField10.getText());

                    found = true;
                    break;
                }
            }

            if (!found) {
                JOptionPane.showMessageDialog(this, "Patient not found!");
                return;
            }

            try (FileWriter writer = new FileWriter(filePath)) {
                jsonObject.put("patientList", patientList);
                writer.write(jsonObject.toJSONString());
                writer.flush();
            }

            JOptionPane.showMessageDialog(this, "Patient record updated successfully!");
            loadPatients();
            clearEditPatientFields();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating patient record.");
        }
    }

