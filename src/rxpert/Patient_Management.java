    // Load patients into the table
    private void loadPatients() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try (FileReader reader = new FileReader(filePath)) {
            JSONParser parser = new JSONParser();
            JSONObject jsonObject = (JSONObject) parser.parse(reader);
            JSONArray patientList = (JSONArray) jsonObject.get("patientList");

            for (Object obj : patientList) {
                JSONObject patient = (JSONObject) obj;
                model.addRow(new Object[]{
                    patient.get("name"), patient.get("age"), patient.get("dateOfBirth"),
                    patient.get("gender"), patient.get("phone"), patient.get("email")
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

